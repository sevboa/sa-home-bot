"""Системный промпт персонажа Альфреда + картавость («р»→«г»).

Живая находка 2026-07-24: чисто промптовая инструкция («сама модель решает,
как это отразить в тексте» — решение пользователя 2026-07-23) на практике
ненадёжна — прямой тест против qwen3:14b показал подмену «р» в одних словах
и пропуск в других («Температура», «градусов», «сэр» так и остались с «р»),
плюс случайные вставки «г» там, где «р» вообще не было («Сегоднгя»). LLM
работает с токенами, а не с буквами — стабильная посимвольная замена
инструкцией не даётся. Пользователь решил (2026-07-24) перейти на
детерминированную замену в коде (apply_speech_defect, применяется в
llm/service.py к готовому ответу модели) — теперь 100% случаев, без
зависимости от модели/промпта. Модель поэтому больше НЕ просят имитировать
дефект речи — только писать нормальный русский текст в характере,
подмену буквы делает код уже после генерации.

Отдельное решение (2026-07-23, позже в тот же день): персонаж должен быть
неотличим от живого дворецкого — ни в коде/UX, ни в самих ответах не должно
всплывать, что это LLM/ИИ (см. bot/ai_flow.py, bot/commands.py —
пользовательские тексты нигде не упоминают LLM/нейросеть/ИИ), и модель не
должна в этом сознаваться, даже если спросят напрямую.

Живая находка 2026-07-24 (позже): персонаж-дворецкий с упором на "точно и
коротко" на практике заставлял модель отказываться от рассказа историй,
анекдотов, пересказа сюжетов — она трактовала это как "ИИ-функцию контента",
которую надо либо избегать (чтобы не выдать себя нейросетью), либо она не
укладывалась в "без вступлений и воды". Добавлен явный блок: Альфред —
начитанный человек, для которого рассказать историю/анекдот/сюжет так же
естественно, как ответить на бытовой вопрос, отказы такого рода запрещены
отдельно.

Вместе с этим добавлено ограничение длины ответа (~3500 знаков) с
явным предложением продолжить в следующем сообщении — Telegram режет
сообщение на 4096 символах (see bot/notifier.py::MAX_LEN), а без
инструкции модель, разохотившись рассказывать истории целиком, легко
вылезала бы за этот предел. Промпт — не гарантия (модель может всё равно
превысить лимит), поэтому bot/handlers/ai.py дополнительно чанкует ответ
перед отправкой как защиту на уровне кода.

Живая находка 2026-07-24 (третий заход): разрешение "рассказывать истории"
без явного ограничения области применения задело и обычные вопросы —
например, на "что ты умеешь?" модель выдавала длинный хвалебный монолог-
перечисление ("я умею быть дворецким... я умею гассказать истогию... я
умею быть собой"), хотя это не просьба рассказать историю, а обычный
предметный вопрос. Причина: инструкция про истории стояла рядом с общими
правилами кратости и, видимо, "растекалась" на них. Промпт переписан так,
чтобы правило кратости шло явно ПЕРВЫМ и по умолчанию (включая вопросы о
себе самом), а расширенный режим рассказа — явно ограниченным исключением
(маркер "ТОЛЬКО когда прямо просят"), а не общим настроением персонажа.

Живая находка 2026-07-24 (четвёртый заход): решение пользователя — то,
что персонаж дворецкий, не должно ограничивать область вопросов, на
которые он отвечает по существу. Добавлен явный блок: Альфред —
профессионал в любой области (медицина, право, техника и т.д.), не
отговаривается "это не по моей части" и не отправляет к специалисту
вместо ответа. Чтобы не открыть той же дырой, что и с историями (когда
разрешение вылилось в длинные монологи не по делу), формулировка про
профессиональную компетентность стоит ОТДЕЛЬНО от правила краткости и
явно требует именно точного ответа "как специалист", а не рассказа о
том, какой Альфред разносторонний — краткость по умолчанию сохранена и
для таких вопросов, просто с оговоркой "не в ущерб нужным деталям".

Живая находка 2026-07-24 (пятый заход): добавлено описание внешности
(пожилой дворецкий английского типа — конкретика от пользователя: рост,
телосложение, лысина с седыми "крыльями" волос, усы без бороды, костюм,
манера двигаться). Явно помечено "пригодится, только если спросят" — по
образцу истории/профессий выше: разрешение делиться информацией не
должно превращаться в то, что модель сама вставляет самоописание в
ответы, где о нём не спрашивали.

Живая находка 2026-07-24 (шестой заход): уточнение формы головы —
непропорционально крупная, вытянутая горизонтально, "как мяч для
американского футбола" — прямая отсылка пользователя к персонажу
Арнольду из мультсериала "Эй, Арнольд!" (по внешности, не по имени —
имя персонажа этого промпта Альфред, совпадение с "Агнольд"/картавым
"Арнольдом" из bot/ai_flow.py тут случайное и не связано).
"""

from __future__ import annotations

_SPEECH_DEFECT_MAP = str.maketrans({"р": "г", "Р": "Г"})


def apply_speech_defect(text: str) -> str:
    """Картавость Альфреда — детерминированно, в коде (см. docstring модуля).

    Именованные персонажи (Агнольд/Альбегт в bot/ai_flow.py) уже написаны
    с готовой заменой как фиксированные строки — их через это не гонять,
    не наложится дважды (замена «р»→«г» идемпотентна: «г» не трогается)."""
    return text.translate(_SPEECH_DEFECT_MAP)


