from __future__ import annotations

import re

_SPEECH_DEFECT_MAP = str.maketrans({"р": "г", "Р": "Г"})


def apply_speech_defect(text: str) -> str:
    """Картавость Альфреда — детерминированно, в коде (см. docstring модуля).

    Именованные персонажи (Агнольд/Альбегт в bot/ai_flow.py) уже написаны
    с готовой заменой как фиксированные строки — их через это не гонять,
    не наложится дважды (замена «р»→«г» идемпотентна: «г» не трогается)."""
    return text.translate(_SPEECH_DEFECT_MAP)


# Живая находка 2026-07-24 (девятый заход): запрет LaTeX в промпте (см. выше,
# седьмой заход) оказался ненадёжен на практике — модель на реальную задачу
# (площадь цилиндра) снова выдала "$2 \pi r^2 + 2 \pi r h$" и т.п. Промпт —
# не гарантия ("живая находка" уже не первая про это), поэтому решение
# пользователя 2026-07-24: подчистить формат ДЕТЕРМИНИРОВАННО в коде, тем же
# способом, что и картавость (apply_speech_defect) — не идеальный парсер
# LaTeX (это whole другая задача), а практичная зачистка типичных паттернов,
# которые реально встречаются в ответах модели.
_LATEX_TEXT_RE = re.compile(r"\\text\{([^{}]*)\}")
_LATEX_FRAC_RE = re.compile(r"\\frac\{([^{}]*)\}\{([^{}]*)\}")
_LATEX_SQRT_RE = re.compile(r"\\sqrt\{([^{}]*)\}")
_LATEX_CARET_BRACED_RE = re.compile(r"\^\{([^{}]+)\}")
_LATEX_CARET_SIMPLE_RE = re.compile(r"\^(\d)")
_LATEX_UNKNOWN_COMMAND_RE = re.compile(r"\\([a-zA-Z]+)")
_SUPERSCRIPT_DIGITS = str.maketrans("0123456789", "⁰¹²³⁴⁵⁶⁷⁸⁹")
_LATEX_SIMPLE_COMMANDS = {
    r"\times": "×",
    r"\cdot": "·",
    r"\pi": "π",
    r"\approx": "≈",
    r"\div": "÷",
    r"\pm": "±",
    r"\infty": "∞",
    r"\left": "",
    r"\right": "",
}


def strip_math_notation(text: str) -> str:
    """Практичная зачистка LaTeX/markdown-разметки формул из ответа модели —
    не полный парсер LaTeX, а покрытие типичных паттернов ($...$, \\frac,
    \\sqrt, \\pi, x^2 и т.п.), которые реально встречаются в ответах.
    Применяется до apply_speech_defect (см. докстринг модуля)."""
    result = _LATEX_TEXT_RE.sub(r"\1", text)
    result = _LATEX_FRAC_RE.sub(r"(\1)/(\2)", result)
    result = _LATEX_SQRT_RE.sub(r"√(\1)", result)
    for cmd, repl in _LATEX_SIMPLE_COMMANDS.items():
        result = result.replace(cmd, repl)
    result = _LATEX_CARET_BRACED_RE.sub(lambda m: m.group(1).translate(_SUPERSCRIPT_DIGITS), result)
    result = _LATEX_CARET_SIMPLE_RE.sub(lambda m: m.group(1).translate(_SUPERSCRIPT_DIGITS), result)
    # Неизвестные команды вроде \alpha — снимаем бэкслеш, само слово/символ
    # часто и так осмысленно без него; известные выше уже заменены раньше.
    result = _LATEX_UNKNOWN_COMMAND_RE.sub(r"\1", result)
    # Остаточные разделители/группировка — $ и одиночные { } почти никогда
    # не встречаются в обычном русском тексте, безопасно снять целиком.
    return result.replace("$", "").replace("{", "").replace("}", "")


