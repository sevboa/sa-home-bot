"""Системный промпт персонажа Альфреда + картавость («р»→«г»).

Живая находка 2026-07-24: чисто промптовая инструкция («сама модель решает,
как это отразить в тексте» — решение пользователя 2026-07-23) на практике
ненадёжна — прямой тест против qwen3:14b показал подмену «р» в одних словах
и пропуск в других («Температура», «градусов», «сэр» так и остались с «р»),
плюс случайные вставки «г» там, где «р» вообще не было («Сегоднгя»). LLM
работает с токенами, а не с буквами — стабильная посимвольная замена
инструкцией не даётся. Пользователь решил (2026-07-24) перейти на
детерминированную замену в коде (apply_speech_defect, применяется в
llm/service.py к готовому ответу модели) — теперь 100% случаев, без
зависимости от модели/промпта. Модель поэтому больше НЕ просят имитировать
дефект речи — только писать нормальный русский текст в характере,
подмену буквы делает код уже после генерации.

Отдельное решение (2026-07-23, позже в тот же день): персонаж должен быть
неотличим от живого дворецкого — ни в коде/UX, ни в самих ответах не должно
всплывать, что это LLM/ИИ (см. bot/ai_flow.py, bot/commands.py —
пользовательские тексты нигде не упоминают LLM/нейросеть/ИИ), и модель не
должна в этом сознаваться, даже если спросят напрямую.
"""

from __future__ import annotations

_SPEECH_DEFECT_MAP = str.maketrans({"р": "г", "Р": "Г"})


def apply_speech_defect(text: str) -> str:
    """Картавость Альфреда — детерминированно, в коде (см. docstring модуля).

    Именованные персонажи (Агнольд/Альбегт в bot/ai_flow.py) уже написаны
    с готовой заменой как фиксированные строки — их через это не гонять,
    не наложится дважды (замена «р»→«г» идемпотентна: «г» не трогается)."""
    return text.translate(_SPEECH_DEFECT_MAP)


